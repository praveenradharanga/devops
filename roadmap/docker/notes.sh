### My recommendation for you

Since you're already learning:

* Linux
* AWS EC2
* Git/GitHub
* Terraform

Spend **7–10 days on Docker**, not months.

A practical roadmap:

```text
Linux      ✅
Git/GitHub ✅
AWS EC2    ✅
Docker     ← Current focus
Jenkins    ← Next
Terraform  ← Continue
CI/CD      ← After Jenkins
Kubernetes ← Later
```

Don't try to master Docker completely before moving on. Learn enough to:

1. Build an image.
2. Run containers.
3. Use volumes and ports.
4. Run Jenkins and Nginx in Docker.

That's enough to support your transition from Support Engineer toward DevOps. 🚀

